/** 
* Program berikut untuk mencoba operator aritmetika
*/
public class soal1 {
    public static void main(String args[]) {
        double num1 = 3;
        double num2 = 2;
        System.out.println("num1 + num2 = " + (int)(num1 + num2) );
        System.out.println("num1 - num2 = " + (int)(num1 - num2) );
        System.out.println("num1 * num2 = " + (int)(num1 * num2) );
        System.out.println("num1 / num2 = " + (num1 / num2) );
        System.out.println("num1 % num2 = " + (int)(num1 % num2) );
        }
}
