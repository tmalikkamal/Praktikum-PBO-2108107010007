/** 
* Program berikut untuk mencoba operator aritmetika
*/
public class soal1 {
    public static void main(String args[]) {
        int num1 = 3;
        int num2 = 2;
        System.out.println("num1 + num2 = " + (num1 + num2) );
        System.out.println("num1 - num2 = " + (num1 - num2) );
        System.out.println("num1 * num2 = " + (num1 * num2) );
        System.out.println("num1 / num2 = " + ((double)num1 / num2) );
        System.out.println("num1 % num2 = " + (num1 % num2) );
        }
}
